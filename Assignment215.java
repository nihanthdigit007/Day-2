public class Assignment215 {
        public static void main(String[] args) {
            int a = 10;
            int b = 5;

            a += b;
            System.out.println("After += operation, a = " + a);

            a -= b;
            System.out.println("After -= operation, a = " + a);

            a *= b;
            System.out.println("After *= operation, a = " + a);

            a /= b;
            System.out.println("After /= operation, a = " + a);

            a %= b;
            System.out.println("After %= operation, a = " + a);
    }

}
